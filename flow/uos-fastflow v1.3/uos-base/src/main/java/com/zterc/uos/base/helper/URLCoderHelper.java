package com.zterc.uos.base.helper;

import java.io.UnsupportedEncodingException;
import java.net.URLDecoder;
import java.net.URLEncoder;

public class URLCoderHelper {
	public static String encode(String param)
			throws UnsupportedEncodingException {
		return URLEncoder.encode(param, "GBK");
	}

	public static String decode(String param) throws UnsupportedEncodingException {
		return URLDecoder.decode(param, "GBK");
	}
}
