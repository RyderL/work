package com.zterc.uos.base.exception;

public class UOSExeption extends RuntimeException {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public UOSExeption() {
	}

	public UOSExeption(String message) {
		super(message);
	}

	public UOSExeption(String message, Throwable throwable) {
		super(message, throwable);
	}

	public UOSExeption(Throwable throwable) {
		super(throwable);
	}
}
