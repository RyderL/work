package com.ztesoft.uosflow.core.dto.server;

import java.util.Map;

import com.ztesoft.uosflow.core.dto.CommandDto;

public class CostTimeDto extends CommandDto {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	private long costTime;
	
	private String flowCommand;
	
	public long getCostTime() {
		return costTime;
	}


	public void setCostTime(long costTime) {
		this.costTime = costTime;
	}


	public String getFlowCommand() {
		return flowCommand;
	}


	public void setFlowCommand(String flowCommand) {
		this.flowCommand = flowCommand;
	}


	public CostTimeDto(){
		this.setCommandCode("costTime");
	}

	
	@Override
	public void init(Map<String, Object> paramsMap) {
		super.init(paramsMap);
	}
	
	
}
